     private void cargarElementos(){
         // primero se cargan los tipos de item disponibles
            ArrayList<TipoItem> tiposItemDisponibles =  generadorProyectos.getTiposItem();
            DefaultListModel<String> dlm = new DefaultListModel();
            for(TipoItem t:tiposItemDisponibles){
                  dlm.addElement(t.getNombre());
            }
                  jListTiposItemDisponibles.setModel(dlm);
         /* luego los integrantes del equipo que est√© trabajando con el proyecto para
            ser los responsables */
           ArrayList<Usuario> posiblesResponsables = equipoItem.getIntegrantes();
                  dlm = new DefaultListModel();
           for(Usuario u:posiblesResponsables){
                  dlm.addElement(u.getNombre());
           }      
               jListPosiblesResponsables.setModel(dlm);
               
               jComboBoxPrioridad.addItem("BAJA");
               jComboBoxPrioridad.addItem("MEDIA");
               jComboBoxPrioridad.addItem("ALTA");
               
               jLabelValidarDatos.setForeground(Color.red);
      }
