    private void jButtonCrearItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearItemActionPerformed
        // TODO add your handling code here:
        // se deben validar los datos, por ejemplo, que el item no tenga el mismo nombre
         String nombreNuevoItem = jTextFieldNombreItem.getText();
           if ((nombreNuevoItem.isEmpty())|(jListTiposItemDisponibles.getSelectedIndex()<0)|(jComboBoxPrioridad.getSelectedIndex()<0)|(jListPosiblesResponsables.getSelectedIndex()<0)){
               jLabelValidarDatos.setText("Debe completar los campos");
           }else
               // el nombre no puede ser igual s√≥lamente dentro del proyecto
               if(proyectoActual.getItem(nombreNuevoItem) != null){
                  JOptionPane.showMessageDialog((JFrame)SwingUtilities.getWindowAncestor(this),"Ya existe un item con ese nombre dentro del proyecto"); 
               }
               else{
                  // se busca al frame padre para poder conseguir el usuario que est√° utilizando el programa 
                  FramePrincipal frameActual = (FramePrincipal)SwingUtilities.getWindowAncestor(this);
                  Usuario uL = frameActual.getUsuarioLogueado();
                  
                  String nombreTipo = jListTiposItemDisponibles.getSelectedValue();
                  String prioridadNuevoItem = (String)jComboBoxPrioridad.getSelectedItem();
                  TipoItem tipoSeleccionado = generadorProyectos.getTipoItem(nombreTipo);
                  Item nuevoItemCreado = new Item(nombreNuevoItem,tipoSeleccionado,prioridadNuevoItem,uL);
                          nuevoItemCreado.setResponsable(generadorUsuarios.getUsuario(jListPosiblesResponsables.getSelectedValue()));
                          proyectoActual.addItem(nuevoItemCreado);
                  JOptionPane.showMessageDialog((JFrame)SwingUtilities.getWindowAncestor(this),"Se ha creado el item exitosamente");
                  jTextFieldNombreItem.setText("");
                  jListTiposItemDisponibles.setSelectedIndex(0);
                  jComboBoxPrioridad.setSelectedIndex(0);
                  jListPosiblesResponsables.setSelectedIndex(0);
               }
    }//GEN-LAST:event_jButtonCrearItemActionPerformed

