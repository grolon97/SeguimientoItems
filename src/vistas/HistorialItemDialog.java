    private void jButtonBuscarResponsableActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarResponsableActionPerformed
        // TODO add your handling code here:
         Date d = jXDatePicker1.getDate();
         if(d != null){
                    String estadoBuscado = (String)jComboBoxEstadosItem.getSelectedItem();
                    int mes = d.getMonth() + 1;
                    int ano = d.getYear() + 1900;
                    ArrayList<Usuario> responsablesItem = itemInfo.getResponsablesByFecha(estadoBuscado, d.getDate(), mes,ano);
                             if(responsablesItem == null){
                                      jLabelResponsables.setForeground(Color.red);
                                      jLabelResponsables.setText("No se han encontrado resultados");
                             }         
                             else{
                                      jLabelResponsables.setForeground(Color.green);
                                      jLabelResponsables.setText("Se han encontrado " + responsablesItem.size() + " resultados");
                                      DefaultListModel<String> dlm = new DefaultListModel<>();
                                      for(Usuario r:responsablesItem){
                                           dlm.addElement(r.getNombre());
                                      }
                                      jListResponsables.setModel(dlm);
                             }   
         }else
                  JOptionPane.showMessageDialog((JFrame)SwingUtilities.getWindowAncestor(this),"Seleccione una fecha");
                          
                  
    }//GEN-LAST:event_jButtonBuscarResponsableActionPerformed

