    private void cargarElementos(){
         jLabelResponsables.setText("");
         jLabelNombreItem.setText(itemInfo.getNombre());
        /* 
           Se cargan todos los historiales, mostrando la secuencia en orden cronológico. 
           Esto se debe a que el método getHistoriales() debería retornar ordenado por fecha 
        */     
         ArrayList<Historial> historialesItem = itemInfo.getHistoriales();
                  DefaultTableModel model = (DefaultTableModel) jTableHistoriales.getModel();
                  String nombreResponsable;
                  for(Historial h:historialesItem){
                           if(h.getResponsable() == null)
                                    nombreResponsable = "Sin Responsable";
                           else nombreResponsable = h.getResponsable().getNombre();
                                    model.addRow(new Object [] {h.getEstado().getEstado(),nombreResponsable,h.getFecha().toString()});
                  }
                  
         TipoItem ti = itemInfo.getTipo();
         ArrayList<Estado> estados =  ti.getEstados();
         
                  for(Estado e:estados){
                    jComboBoxEstadosItem.addItem(e.getEstado());
                  }
    }
